    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = Main.employeeTable.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel employeeListModel = (DefaultTableModel) Main.employeeTable.getModel();
            //Employee employeeToEdit = null;
            Object value = employeeListModel.getValueAt(selectedRow, ID).toString();
            Integer employeeID = Integer.parseInt(value.toString());
            Employee foundEmployee = EmployeeService.getByID(employeeID);
            Edit editWindow = new Edit(foundEmployee);
            editWindow.setVisible(true);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = Main.employeeTable.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel employeeListModel = (DefaultTableModel) Main.employeeTable.getModel();
            Object value = employeeListModel.getValueAt(selectedRow, ID).toString();
            Integer employeeID = Integer.parseInt(value.toString());
            employeeListModel.removeRow(selectedRow);
            EmployeeService.delete(employeeID);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

