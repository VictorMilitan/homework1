    public static void editEmployee(int idEmployee, String newName, String newSurname, String birth, String newStreet, String newCity, String newCountry, String newZip, String newPosition) {
        DefaultTableModel employeeListModel = (DefaultTableModel) Main.employeeTable.getModel();
        employeeListModel.setValueAt(newName, idEmployee - 1, 1); // тут мы определяем куда будем кидать новую 1) новая инфа 2)на какую строчку 3)какой столбец
        employeeListModel.setValueAt(newSurname, idEmployee - 1, 2);
        employeeListModel.setValueAt(birth, idEmployee - 1, 3);
        employeeListModel.setValueAt(newStreet + " " + newCity + " " + newCountry + " " + newZip, idEmployee - 1, 4);
        employeeListModel.setValueAt(newPosition, idEmployee - 1, 5);
        //вызвать employee service
        EmployeeService.edit(idEmployee, newName, newSurname, birth, newStreet, newCity, newCountry, newZip, newPosition);
    }

