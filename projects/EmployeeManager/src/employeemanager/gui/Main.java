    private static void addEmployeeToList(Employee emp) throws SQLException {
        DefaultTableModel employeeListModel = (DefaultTableModel) Main.employeeTable.getModel(); //так ссылаемся на наши данные которые мы указываем в таблице
        int employeeID = employeeListModel.getRowCount() + 1;
        employeeListModel.addRow(new Object[]{employeeID, emp.getName(), emp.getSurname(), emp.getBirth(), emp.getStreet() + " " + emp.getCity() + " " + emp.getCountry() + " " + String.valueOf(emp.getZip()), emp.getPosition().toString()});
        emp.setId(employeeID);
        EmployeeService.add(emp);

    }

